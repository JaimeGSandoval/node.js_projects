exports.getAllTours = async (req, res) => {
  try {
    // find() will return an array of all the tours and convert them into js objects
    const tours = await Tour.find();

    res.status(200).json({
      status: 'success',
      results: tours.length,
      data: {
        tours,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err,
    });
  }
};

